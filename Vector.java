import java.lang.*;
import java.util.*;

public class Vector {
  //Attributes
  private int x;
  private int y;

  //Constructor
  public Vector(int x,int y)
  {
    this.x = x;
    this.y = y;
  }

  //Methods
  public void setX(int x) {this.x = x;}

  /***************************************************/

  public void setY(int y) {this.y = y;}

  /***************************************************/

  public int getX() {return x;}

  /***************************************************/

  public int getY() {return y;}

  /***************************************************/




}
